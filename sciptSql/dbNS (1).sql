CREATE TABLE VAITRO (
    MAVAITRO NUMBER(3) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENVAITRO NVARCHAR2(50) NOT NULL
);

CREATE TABLE PTTT (
    MAPTTT NUMBER(3) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENPTTT NVARCHAR2(50) NOT NULL,
    MOTA NVARCHAR2(255)
);

CREATE TABLE LOAISP (
    MALOAI NUMBER(5) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENLOAI NVARCHAR2(100) NOT NULL
);

CREATE TABLE TAIKHOAN (
    MATK NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMAIL VARCHAR2(100) NOT NULL UNIQUE,
    MATKHAU VARCHAR2(255) NOT NULL,
    HOTEN NVARCHAR2(100) NOT NULL,
    SDT VARCHAR2(15),
    MAVAITRO NUMBER(3) NOT NULL,
    CONSTRAINT FK_TK_VAITRO FOREIGN KEY (MAVAITRO) REFERENCES VAITRO(MAVAITRO)
);

CREATE TABLE CUAHANG (
    MACH NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENCH NVARCHAR2(150) NOT NULL,
    DIACHI NVARCHAR2(255),
    TRANGTHAI NVARCHAR2(50) DEFAULT 'HOẠT ĐỘNG',
    MATK NUMBER(10) NOT NULL UNIQUE,
    CONSTRAINT FK_CH_TK FOREIGN KEY (MATK) REFERENCES TAIKHOAN(MATK)
);

CREATE TABLE SANPHAM (
    MASP NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENSP NVARCHAR2(255) NOT NULL,
    MOTA CLOB,
    DONGIA NUMBER(15) NOT NULL,
    SOLUONGTON NUMBER(10) DEFAULT 0,
    HINHANH VARCHAR2(255),
    MALOAI NUMBER(5) NOT NULL,
    MACH NUMBER(10) NOT NULL,
    CONSTRAINT FK_SP_LOAI FOREIGN KEY (MALOAI) REFERENCES LOAISP(MALOAI),
    CONSTRAINT FK_SP_CH FOREIGN KEY (MACH) REFERENCES CUAHANG(MACH)
);

CREATE TABLE GIOHANG (
    MAGH NUMBER(12) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NGAYCAPNHAT TIMESTAMP DEFAULT SYSTIMESTAMP,
    MATK NUMBER(10) NOT NULL UNIQUE,
    CONSTRAINT FK_GH_TK FOREIGN KEY (MATK) REFERENCES TAIKHOAN(MATK)
);

CREATE TABLE CTGH (
    MAGH NUMBER(12) NOT NULL,
    MASP NUMBER(10) NOT NULL,
    SOLUONG NUMBER(5) NOT NULL,
    PRIMARY KEY (MAGH, MASP),
    CONSTRAINT FK_CTGH_GH FOREIGN KEY (MAGH) REFERENCES GIOHANG(MAGH) ON DELETE CASCADE,
    CONSTRAINT FK_CTGH_SP FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP)
);

CREATE TABLE HOADON (
    MAHD NUMBER(15) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MATK NUMBER(10) NOT NULL,
    MAPTTT NUMBER(3) NOT NULL,
    NGAYLAP TIMESTAMP DEFAULT SYSTIMESTAMP,
    TONGTIEN NUMBER(15) NOT NULL,
    TRANGTHAI NVARCHAR2(50) DEFAULT 'CHỜ XÁC NHẬN',
    DIACHIGIAO NVARCHAR2(255) NOT NULL,
    CONSTRAINT FK_HD_TK FOREIGN KEY (MATK) REFERENCES TAIKHOAN(MATK),
    CONSTRAINT FK_HD_PTTT FOREIGN KEY (MAPTTT) REFERENCES PTTT(MAPTTT)
);

CREATE TABLE CTHD (
    MAHD NUMBER(15) NOT NULL,
    MASP NUMBER(10) NOT NULL,
    SOLUONG NUMBER(5) NOT NULL,
    DONGIALUCMUA NUMBER(15) NOT NULL,
    PRIMARY KEY (MAHD, MASP),
    CONSTRAINT FK_CTHD_HD FOREIGN KEY (MAHD) REFERENCES HOADON(MAHD),
    CONSTRAINT FK_CTHD_SP FOREIGN KEY (MASP) REFERENCES SANPHAM(MASP)
);

